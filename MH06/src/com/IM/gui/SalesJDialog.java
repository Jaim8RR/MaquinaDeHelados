    private void updateTablaVentas() {
        try {
                    ArrayList<Sales> sales = IM.showSales();
                    DefaultTableModel m = (DefaultTableModel) jTableVentas.getModel();
                    
                    for (Sales sale : sales) {
                 Object[] o = {sale.getTime(),sale.getPosition(),sale.getName(),df.format(sale.getPrice()),sale.getType(),sale.getQuantity()};
                        m.addRow(o);
            }
                } catch (Exception ex) {
                    System.out.println("Error al recuperar ventas");
                }
    }
